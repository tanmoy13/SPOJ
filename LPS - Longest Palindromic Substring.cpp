string process(string &str)
{
    string ret;
    ret+='$';
    ret+='#';
    for(int i=0;i<SZ(str);i++)
    {
       ret+=str[i];
       ret+='#';
    }
    ret+='@';
    return ret;
//    cout<<str<<endl;
}

int manacher(string &str)
{
    int c=0,r=0;
    for(int i=1;i<SZ(str)-1;i++)
    {
        int miror=c+c-i;
        if(i<r)
            p[i]=min(p[miror],r-i);
        while(str[i+1+p[i]]==str[i-1-p[i]])
            p[i]++;
        if(i+p[i]>r)
        {
            c=i;
            r=i+p[i];
        }
    }
    int ret=0;
    for(int i=0;i<SZ(str);i++)
        ret=max(ret,p[i]);
    return ret;
}

